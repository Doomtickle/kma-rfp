require 'test_helper'

class ProposalsControllerTest < ActionController::TestCase
  setup do
    @proposal = proposals(:one)
  end

  test "should get index" do
    get :index
    assert_response :success
    assert_not_nil assigns(:proposals)
  end

  test "should get new" do
    get :new
    assert_response :success
  end

  test "should create proposal" do
    assert_difference('Proposal.count') do
      post :create, proposal: { bonus: @proposal.bonus, cpps: @proposal.cpps, purchased_30: @proposal.purchased_30, purchased_60: @proposal.purchased_60, rfp_amount: @proposal.rfp_amount, user_id: @proposal.user_id }
    end

    assert_redirected_to proposal_path(assigns(:proposal))
  end

  test "should show proposal" do
    get :show, id: @proposal
    assert_response :success
  end

  test "should get edit" do
    get :edit, id: @proposal
    assert_response :success
  end

  test "should update proposal" do
    patch :update, id: @proposal, proposal: { bonus: @proposal.bonus, cpps: @proposal.cpps, purchased_30: @proposal.purchased_30, purchased_60: @proposal.purchased_60, rfp_amount: @proposal.rfp_amount, user_id: @proposal.user_id }
    assert_redirected_to proposal_path(assigns(:proposal))
  end

  test "should destroy proposal" do
    assert_difference('Proposal.count', -1) do
      delete :destroy, id: @proposal
    end

    assert_redirected_to proposals_path
  end
end
